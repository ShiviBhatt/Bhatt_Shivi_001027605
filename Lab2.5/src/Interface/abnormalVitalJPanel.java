    private void detailButton_abnormalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_detailButton_abnormalActionPerformed
        // TODO add your handling code here:
        int selectedRow = vitalSignsTable_abnormal.getSelectedRow();

        if (selectedRow >= 0) {
            VitalSigns vs = (VitalSigns) vitalSignsTable_abnormal.getValueAt(selectedRow, 0);
            
            tempTextField_abnormal.setText(vs.getTemperature() + "");
            bloodTextField_abnormal.setText(vs.getBloodPressure() + "");
            pulseTextField_abnormal.setText(vs.getPulse() + "");
            dateTextField_abnormal.setText(vs.getDate());
        } else {
            JOptionPane.showMessageDialog(null, "Please select a row.");
        }
    }//GEN-LAST:event_detailButton_abnormalActionPerformed

    private void deleteButton_abnormalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButton_abnormalActionPerformed
        // TODO add your handling code here:
        int selectedRow = vitalSignsTable_abnormal.getSelectedRow();

        if (selectedRow >= 0) {
            VitalSigns vs = (VitalSigns) vitalSignsTable_abnormal.getValueAt(selectedRow, 0);
            vsh.deleteVital(vs);
            JOptionPane.showMessageDialog(null, "Vital Sign deleted.");
            populateTable();
        } else {
            JOptionPane.showMessageDialog(null, "Please select a row.");
        }
        tempTextField_abnormal.setText("");
        bloodTextField_abnormal.setText("");
        pulseTextField_abnormal.setText("");
        dateTextField_abnormal.setText("");
    }//GEN-LAST:event_deleteButton_abnormalActionPerformed

