    private void populateTable() {
        DefaultTableModel dtm = (DefaultTableModel) vitalSignsTable_abnormal.getModel();
        dtm.setRowCount(0);
        abnormalList=vsh.getAbnormalList(max, min);
        for (VitalSigns vs :abnormalList) {
            Object row[] = new Object[2];
            row[0] = vs;
            row[1] = vs.getBloodPressure();
            dtm.addRow(row);
        }
    }
